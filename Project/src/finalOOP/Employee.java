package finalOOP;

import java.util.*;



public abstract class Employee extends User {
    private double salary;
    private int yearOfJoin;

    public Employee(String email, String password, String id, String firstName, String lastName,double salary, int yearOfJoin) {
        super(email, password, id, firstName, lastName);
        this.salary = salary;
        this.yearOfJoin = yearOfJoin;
    }

    public double getSalary() {
        return salary;
    }

    public int getYearOfJoin() {
        return yearOfJoin;
    }
}
